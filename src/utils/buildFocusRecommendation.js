import { getPositionTrack } from '../data/positions.js'
import { EXPERIENCE } from './selectAnchorQuestions.js'

/** מבנה מומלץ להליך הראיון (תמיד מוצג אחרי הפתיחה) */
export const INTERVIEW_STRUCTURE_STEPS = [
  'הצגה עצמית של המראיין ותפקידו.',
  'הסבר מבנה הראיון למועמד.ת ("אתה תספר, אחר כך אני, יהיה זמן לשאלות").',
  'המראיין שואל מבנק השאלות ובונה לעצמו פרופיל מועמד ע״פ העוגנים.',
  'המראיין מספר בהרחבה על הארגון והתפקיד.',
  'בריף על תהליך הגיוס ולוחות זמנים.',
]

export const FOCUS_INTRO =
  'להלן כיווני מיקוד מומלצים לראיון — מצורף מבנה מומלץ לראיון. בנוסף תמצאו שאלות מותאמות על פי תפקיד. בחרו את השאלות הרלוונטיות.'

/**
 * המלצות מיקוד למראיין — מבוססות על הערות, תפקיד ורמת ניסיון (ללא AI).
 * @param {{ notes: string, positionLabel: string, positionId: string, candidateExperience: string }} p
 */
export function buildFocusRecommendation({
  notes = '',
  positionLabel = '',
  positionId = '',
  candidateExperience = EXPERIENCE.EARLY,
}) {
  const t = notes.trim().toLowerCase()
  const bullets = []
  const add = (b) => {
    if (b && !bullets.includes(b)) bullets.push(b)
  }

  const track = getPositionTrack(positionId)
  const experienced = candidateExperience === EXPERIENCE.EXPERIENCED

  if (positionLabel) {
    add(
      `התאימו דוגמאות מהעבר לתפקיד «${positionLabel}»: עבודה מול אנשים, עצמאות, ועמידה בנהלים של הארגון.`,
    )
  }

  if (track === 'entry') {
    add(
      experienced
        ? 'מועמד.ת עם ניסיון: עמקו בסיפורי התנהגות אמיתיים ובהבחנה בין מוטיבציה חיצונית לפנימית.'
        : 'מועמד.ת בשלבי כניסה: בדקו התמדה, גישה ללמידה, ואיך מתמודדים עם הוראות ומשוב — לא רק “מה יודע”.',
    )
  } else if (track === 'professional') {
    add(
      'מסלול מקצועי: דגשו על שיקול דעת מקצועי, עבודה במולטי־דיסציפלינריות, ותיעדוף תחת עומס.',
    )
  } else if (track === 'managerial') {
    add(
      'מסלול ניהולי: בחנו תקשורת עם צוותים, קבלת החלטות, והפרדה בין “לעשות” לבין “לנהל”.',
    )
  }

  if (/ללא ניסיון|בלי ניסיון|ראשונ|עבודה ראשונה|סטודנט|מחפש.*ראשון/.test(t)) {
    add(
      'מופיע כמי עם מעט ניסיון תעסוקתי: חפשו סימני אחריות, התמדה בהתנדבות/לימודים/ספורט, ויכולת לקבל משוב.',
    )
  }

  if (/ניסיון|ותק|שנים|תפקיד קודם|עבדתי|עבדת|מקצועי|מנוסה/.test(t)) {
    add('מצוינות ניסיון בהערות: עמקו באירועים קונקרטיים, בתפקידים ובתוצאות — ומה למד מהם.')
  }

  if (/צבא|שירות לאומי|מילואים|גיוס/.test(t)) {
    add('רקע צבאי/שירות: אפשר לבחון עמידה בסמכות, עבודת צוות תחת כללים, והתמודדות עם שינויי משימה.')
  }

  if (/לחץ|עומס|מולטי|במקביל|דדליין|מועדים/.test(t)) {
    add('מוזכר לחץ/עומס: הדגישו עוגני חובה ופרואקטיביות — איך מסדרים עדיפויות ומתקשרים כשלא מספיקים.')
  }

  if (/קונפליקט|קשה להסתדר|ריב|מחלוקת|לא הסכמתי/.test(t)) {
    add('מוזכרת דינמיקה בינאישית מאתגרת: התמקדו בעוגן ארגון וחברתיות — עובדות, לא רק עמדות.')
  }

  if (
    /מדריך|מדריכ|הדרכה|ילדים|מתנס|חוג|קייטנה|קהילה|מועדונית|הוסטל|מיתל|אלוטף/.test(t) ||
    positionId === 'guide' ||
    positionId.startsWith('instructor_') ||
    positionId.startsWith('assistant_')
  ) {
    add(
      'תפקיד מול אוכלוסייה / הדרכה: בדקו סבלנות, גבולות מקצועיים, ודוגמה למצב שבו נדרשה יצירתיות תחת מגבלה.',
    )
  }

  if (/אוטיסט|על הרצף|מיוחד|התמחות|טיפול|ליווי/.test(t)) {
    add('רקע רגישות לאוכלוסייה: שימו לב לשפה מכבדת, לדוגמאות של סבלנות ושיתוף פעולה עם צוות.')
  }

  return {
    intro: FOCUS_INTRO,
    structureSteps: INTERVIEW_STRUCTURE_STEPS,
    bullets,
  }
}
